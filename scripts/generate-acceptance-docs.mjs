import {
  AstBuilder,
  GherkinClassicTokenMatcher,
  Parser,
} from "@cucumber/gherkin";
import { IdGenerator } from "@cucumber/messages";
import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { format } from "prettier";

const rootDir = path.resolve(fileURLToPath(new URL("..", import.meta.url)));
const featuresDir = path.join(rootDir, "features");
const outputPath = path.join(
  rootDir,
  "src",
  "generated",
  "acceptance-features.ts"
);

const tableFromRows = (rows = []) =>
  rows.map((row) => ({
    id: `line-${row.location.line}`,
    cells: row.cells.map((cell) => ({
      id: `line-${cell.location.line}-column-${cell.location.column}`,
      value: cell.value,
    })),
  }));

const tagsFrom = (node) => node.tags?.map((tag) => tag.name) ?? [];

const hasPendingTag = (tags) => tags.includes("@pending");

const mapStep = (step) => ({
  id: `line-${step.location.line}`,
  keyword: step.keyword.trim(),
  text: step.text,
  table: step.dataTable ? tableFromRows(step.dataTable.rows) : undefined,
  docString: step.docString?.content,
});

const mapExamples = (examples) =>
  examples.map((example) => {
    const tags = tagsFrom(example);

    return {
      id: `line-${example.location.line}`,
      name: example.name,
      tags,
      status: hasPendingTag(tags) ? "under-review" : "covered",
      table: tableFromRows([
        ...(example.tableHeader ? [example.tableHeader] : []),
        ...(example.tableBody ?? []),
      ]),
    };
  });

const mapScenario = (scenario, featureTags) => {
  const scenarioTags = tagsFrom(scenario);
  const allTags = [...featureTags, ...scenarioTags];
  const examples = mapExamples(scenario.examples ?? []);
  const scenarioIsPending = hasPendingTag(allTags);

  return {
    id: `line-${scenario.location.line}`,
    keyword: scenario.keyword,
    name: scenario.name,
    description: scenario.description.trim(),
    tags: scenarioTags,
    status: scenarioIsPending ? "under-review" : "covered",
    hasUnderReviewExamples: examples.some(
      (example) => example.status === "under-review"
    ),
    steps: scenario.steps.map(mapStep),
    examples,
  };
};

const mapScenariosFromChildren = (children, inheritedTags) =>
  children.flatMap((child) => {
    if (child.scenario) {
      return [mapScenario(child.scenario, inheritedTags)];
    }

    if (child.rule) {
      return mapScenariosFromChildren(child.rule.children, [
        ...inheritedTags,
        ...tagsFrom(child.rule),
      ]);
    }

    return [];
  });

const buildFeature = async (fileName) => {
  const relativePath = path.posix.join("features", fileName);
  const source = await readFile(path.join(featuresDir, fileName), "utf8");
  const uuid = IdGenerator.uuid();
  const parser = new Parser(
    new AstBuilder(uuid),
    new GherkinClassicTokenMatcher()
  );
  const document = parser.parse(source);
  const feature = document.feature;

  if (!feature) {
    throw new Error(`No feature found in ${relativePath}`);
  }

  const featureTags = tagsFrom(feature);
  const scenarios = mapScenariosFromChildren(feature.children, featureTags);

  return {
    path: relativePath,
    name: feature.name,
    description: feature.description.trim(),
    tags: featureTags,
    status: scenarios.every((scenario) => scenario.status === "under-review")
      ? "under-review"
      : "covered",
    scenarios,
  };
};

const featureFiles = (await readdir(featuresDir))
  .filter((fileName) => fileName.endsWith(".feature"))
  .sort((left, right) => left.localeCompare(right));

const features = await Promise.all(featureFiles.map(buildFeature));

const generatedSource = await format(
  `// Generated by scripts/generate-acceptance-docs.mjs. Do not edit by hand.

export const acceptanceFeatures = ${JSON.stringify(features, null, 2)} as const;
`,
  { parser: "typescript" }
);

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, generatedSource);
